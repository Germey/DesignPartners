function showProject(){
	$('#d_projPreview').css("display","block");
	$('#d_workshop').css("display","none");
	$('#d_traincamp').css("display","none");
}

function showWorkshop(){
	$('#d_projPreview').css("display","none");
	$('#d_workshop').css("display","block");
	$('#d_traincamp').css("display","none");
} 

function showTraincamp(){
	$('#d_projPreview').css("display","none");
	$('#d_workshop').css("display","none");
	$('#d_traincamp').css("display","block");
}